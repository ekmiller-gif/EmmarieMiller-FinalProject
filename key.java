public class key {
    //Attributes
    boolean opensDoor;
    boolean opensWindow;

    /**
     * Constructor
     * @return void
     * @param opensDoor true/false if the key fits the lock in the door
     * @param opensWindow true/false if the key fits the lock in the window
     */
    public void key(){
        opensDoor = this.opensDoor;
        opensWindow = this.opensWindow;
    }
}
